package com.POO.Calculadora_POO;

public class Setters_And_Getters {
    
    double numero1, numero2, soma, subtracao, multiplicacao, divisao, resultado;

    public Setters_And_Getters() {
        this.numero1 = numero1;
        this.numero2 = numero2;
        this.soma = soma;
        this.subtracao = subtracao;
        this.multiplicacao = multiplicacao;
        this.divisao = divisao;
        this.resultado = resultado;
    }

    public double getNumero1() {
        return numero1;
    }

    public void setNumero1(double numero1) {
        this.numero1 = numero1;
    }

    public double getNumero2() {
        return numero2;
    }

    public void setNumero2(double numero2) {
        this.numero2 = numero2;
    }

    public double getSoma() {
        return soma;
    }

    public void setSoma(double soma) {
        this.soma = soma;
    }

    public double getSubtracao() {
        return subtracao;
    }

    public void setSubtracao(double subtracao) {
        this.subtracao = subtracao;
    }

    public double getMultiplicacao() {
        return multiplicacao;
    }

    public void setMultiplicacao(double multiplicacao) {
        this.multiplicacao = multiplicacao;
    }

    public double getDivisao() {
        return divisao;
    }

    public void setDivisao(double divisao) {
        this.divisao = divisao;
    }

    public double getResultado(){
        return resultado;
    }

    public void setResultado(double resultado){
        this.resultado = resultado;
    }
}