package com.POO.Regras_de_voto;

public class Setters_And_Getters {
    
    int idade, titulo;

    public Setters_And_Getters() {
        this.idade = idade;
        this.titulo = titulo;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public int getTitulo() {
        return titulo;
    }

    public void setTitulo(int titulo) {
        this.titulo = titulo;
    }

}